package java_basic.jungol_prob.complate.반복제어문3_c;

public class 반복제어문3_연습문제3 {
    public static void main(String[] args) {

        for (int i=1; i<=3; i++){
                if (i == 1){
                    System.out.println("* ");
                } else if (i == 2) {
                    System.out.println("** ");
                } else {
                    System.out.println("***");
                }
        }

        for (int i=3; i>=1; i--){
                if (i == 3){
                System.out.println("***");
                } else if (i == 2) {
                System.out.println("** ");
                } else {
                System.out.println("* ");
                }
        }







    }
}
